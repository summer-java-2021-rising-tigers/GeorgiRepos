package OptionsMenu;

import javax.swing.*;

public class OptionsMenuFrame {

    JFrame optionsMenu = new JFrame();

    public OptionsMenuFrame(){

        optionsMenu.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        optionsMenu.setExtendedState(JFrame.MAXIMIZED_BOTH);
        optionsMenu.setUndecorated(true);
        optionsMenu.setLayout(null);
        optionsMenu.setVisible(true);

    }


}
