package CharactersMenu;

import javax.swing.*;

public class CharactersMenuFrame {

    JFrame charactersMenu = new JFrame();

    public CharactersMenuFrame(){

        charactersMenu.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        charactersMenu.setExtendedState(JFrame.MAXIMIZED_BOTH);
        charactersMenu.setUndecorated(true);
        charactersMenu.setLayout(null);
        charactersMenu.setVisible(true);

    }

}
